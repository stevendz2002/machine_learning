@app.post("/train")
async def train_endpoint(labels: str = Form(...), files: List[UploadFile] = File(...)):
    global modelo_global, escalador_global

    target_base = "/content/temp_train"
    if os.path.exists(target_base): shutil.rmtree(target_base)
    os.makedirs(target_base)

    label_list = [l.strip() for l in labels.split(",")]

    for i, file in enumerate(files):
        label = label_list[i]
        label_path = os.path.join(target_base, label)
        os.makedirs(label_path, exist_ok=True)

        zip_path = os.path.join(target_base, f"{label}.zip")
        with open(zip_path, "wb") as buffer:
            shutil.copyfileobj(file.file, buffer)

        descomprimir_archivo(zip_path, label_path)
        os.remove(zip_path)

    modelo_global, escalador_global = entrenar_modelo_core(label_list, base_path=target_base)

    return {"status": "Modelo entrenado exitosamente", "etiquetas": label_list}

@app.post("/predict")
async def predict_endpoint(file: UploadFile = File(...)):
    if modelo_global is None:
        return {"error": "El modelo no ha sido entrenado"}

    temp_test = "/content/temp_test"
    if os.path.exists(temp_test): shutil.rmtree(temp_test)
    os.makedirs(temp_test)

    zip_path = "/content/test.zip"
    with open(zip_path, "wb") as buffer:
        shutil.copyfileobj(file.file, buffer)

    descomprimir_archivo(zip_path, temp_test)
    resultados = predecir_lote(modelo_global, escalador_global, temp_test)

    os.remove(zip_path)
    return {"predicciones": resultados}

