import zipfile
import shutil

def descomprimir_archivo(archivo_zip, destino):
    with zipfile.ZipFile(archivo_zip, 'r') as zip_ref:
        zip_ref.extractall(destino)

def entrenar_modelo_core(etiquetas, base_path='/content/train'):
    X, y = procesar(etiquetas, base_path=base_path)
    X_np = np.array(X)

    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X_np)

    model = LogisticRegression(max_iter=1000)
    model.fit(X_scaled, y)

    return model, scaler

def predecir_lote(model, scaler, folder_path):
    resultados = {}
    valid_extensions = ('.jpg', '.jpeg', '.png', '.webp')
    for filename in os.listdir(folder_path):
        if filename.lower().endswith(valid_extensions):
            path = os.path.join(folder_path, filename)
            hist = np.hstack(extraer_hist(path))
            feat = scaler.transform(hist.reshape(1, -1))
            pred = model.predict(feat)
            resultados[filename] = pred[0]
    return resultados

from fastapi import FastAPI, UploadFile, File, Form
