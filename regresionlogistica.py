def extraer_hist (ruta_imagen):
  image = cv2.imread(ruta_imagen)
  hist = cv2.calcHist([image], [0, 1, 2], None, [8, 8, 8], [0, 256, 0, 256, 0, 256])
  return cv2.normalize(hist, hist).flatten()

def procesar(train_labels, base_path='/content/train'):
  y = []
  X = []
  valid_extensions = ('.jpg', '.jpeg', '.png', '.webp')
  for train_label in train_labels:
    directorio = os.path.join(base_path, train_label)
    if not os.path.exists(directorio): continue
    for filename in os.listdir(directorio):
      if filename.lower().endswith(valid_extensions):
        full_path = os.path.join(directorio, filename)
        histograma = np.hstack(extraer_hist(full_path))
        X.append(histograma)
        y.append(train_label)
  return X, y

